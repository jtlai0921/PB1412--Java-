package com.mingrisoft.math;

public class MathUtil {
    public static void main(String[] args) {
        System.out.println("30°的反正弦值：" + Math.asin(Math.PI / 6));// 計算30°的反正弦
        System.out.println("30°的反餘弦值：" + Math.acos(Math.PI / 6));// 計算30°的反餘弦
        System.out.println("30°的反正切值：" + Math.atan(Math.PI / 6));// 計算30°的反正切
    }
}
