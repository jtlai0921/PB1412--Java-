package com.mingrisoft.math;

public class MathUtil {
    public static void main(String[] args) {
        System.out.println("30的雙曲正弦值：" + Math.sinh(30));// 計算30的雙曲正弦函數值
        System.out.println("30的雙曲餘弦值：" + Math.cosh(30));// 計算30的雙曲餘弦函數值
        System.out.println("30的雙曲正切值：" + Math.tanh(30));// 計算30的雙曲正切函數值
    }
}
