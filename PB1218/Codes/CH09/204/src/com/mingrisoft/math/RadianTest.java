package com.mingrisoft.math;

public class RadianTest {
    public static void main(String[] args) {
        System.out.println("30°對應的弧度是：" + Math.toRadians(30));
        System.out.println("π/6對應的角度是：" + Math.toDegrees(Math.PI / 6));
        System.out.println("45°對應的弧度是：" + Math.toRadians(45));
        System.out.println("π/4對應的角度是：" + Math.toDegrees(Math.PI / 4));
    }
}
